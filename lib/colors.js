// ANSI escape codes for coloring text
const BLUE = '\x1b[34m';
const GREEN = '\x1b[32m';
const RED = '\x1b[31m';
const YELLOW = '\x1b[33m';
const CYAN = '\x1b[36m';
const MAGENTA = '\x1b[35m';
const RESET = '\x1b[0m';

module.exports = Object.freeze({ BLUE, CYAN, GREEN, RED, MAGENTA, YELLOW, RESET });
